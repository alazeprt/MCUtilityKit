package top.alazeprt.version;

/**
 * Represents the type of version
 *
 * @author alazeprt
 * @version 1.1
 */
public enum VersionType {
    /**
     * Release version
     */
    RELEASE,
    /**
     * Snapshot version
     */
    SNAPSHOT,
    /**
     * Old beta version
     */
    OLD_BETA,
    /**
     * Old alpha version
     */
    OLD_ALPHA
}
